// Encoding - is the process of converting data, information or signals from one format to another for efficient storage.
// Decoding - is the process of translating received messages into codewords of a given code.

// ASCII (American Standard Code for Onformation Interchange) - a character encoding standard
// that assigns unique numbers (0-127) to represent text, like letters, numbers and symbols allowing
// computers to process and exchange text data consistently.

// Each ASCII character is represented by a 7-bit binary number (2^7 = 128 possible values).

// ASCII was designed smartly:
// - Letters are in order
// - Numbers are in order
// - Uppercase and lowercase differ by 1 bit

// BUT it is limited:
// - Only enough for English
// - Not enough for accents, emojis, non-Latin scripts

// Mental model:
// Bits → Number → ASCII table → Character
